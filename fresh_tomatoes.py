# A single movie entry html template
movie_tile_content = '''
<div class="col-md-12 col-lg-12">
    <div class="container">
        <div class="col-lg-3 movie-tile" data-trailer-id="{trailer_id}" data-toggle="modal" data-target="#trailer">
            <img src="{poster_url}">
        </div>
        <div class="col-lg-9 movie-description">
            <h2>{title} ({year})</h2>
            <ul>
                <li><strong>Content Rating:</strong> {mpaa_rating}</li>
                <li><strong>Duration:</strong> {duration} minutes</li>
                <li><strong>Viewer Rating:</strong> {rating}</li>
                <li><strong>Starring:</strong> {star1} and {star2}</li>
                <li><strong>Synopsis:</strong> {synopsis}</li>
            </ul>
        </div>
    </div>
</div>
'''


def create_movie_tiles_content(movies):
    # The HTML content for this section of the page
    content = ''
    for movie in movies:
        # Append the tile for the movie with its content filled in
        content += movie_tile_content.format(
            title=movie.title,
            poster_url=movie.poster_url,
            trailer_id=movie.trailer_id,
            year=movie.year,
            mpaa_rating=movie.mpaa_rating,
            rating=movie.rating,
            star1=movie.starring[0],
            star2=movie.starring[1],
            duration=movie.duration,
            synopsis=movie.synopsis
        )
    return content


def open_movies_page(movies):
    # Create or overwrite the output file
    output_file = open('fresh_tomatoes.html', 'w')

    # Replace the movie tiles placeholder generated content
    rendered_content = main_page_content.format(
        movie_tiles=create_movie_tiles_content(movies))

    # Output the file
    output_file.write(main_page_head + rendered_content)
    output_file.close()

    # open the output file in the browser (in a new tab, if possible)
    url = os.path.abspath(output_file.name)
    webbrowser.open('file://' + url, new=2)